package org.pytorch.demo.objectdetection;

public class Favorite {
    private String name;
    public int image;

    public Favorite(){
    }

    public Favorite(String name){
        this.name = name;
        this.image = image;

    }

    public String getName() {
        return name;
    }
    public int getImage() { return image;}

    public void setName(String name) {
        this.name = name;
    }

}